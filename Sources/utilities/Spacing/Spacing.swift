    static let s2: CGFloat = base * 1.5  // 12
    static let s3: CGFloat = base * 2  // 16
    static let s4: CGFloat = base * 3  // 24
    static let s5: CGFloat = base * 4  // 32
    static let s6: CGFloat = base * 6  // 48
}

